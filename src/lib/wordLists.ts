export function buildWordList(packId: PackId, curated: W[]): W[] {
  // Deduplicate by word name
  const seen = new Set<string>();
  const merge = (...lists: W[][]): W[] => {
    const result: W[] = [];
    for (const list of lists) {
      for (const w of list) {
        if (!seen.has(w.word)) { seen.add(w.word); result.push(w); }
      }
    }
    return result;
  };

  switch (packId) {
    case 'curated':   return merge(curated);
    case 'top1000':   return merge(curated, TIER1);
    case 'top3000':   return merge(curated, TIER1, TIER2);
    case 'top5000':   return merge(curated, TIER1, TIER2, TIER3);
    case 'top10000':  return merge(curated, TIER1, TIER2, TIER3, TIER4);
    case 'top30000':  return merge(curated, TIER1, TIER2, TIER3, TIER4, TIER5);
    default:          return merge(curated);
  }
}

